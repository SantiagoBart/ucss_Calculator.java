private void formato_Prom(char opc){
String m="";
double p=Double.parseDouble(txtPromFinal.getText());
if(opc=='s'){
    lblMensaje.setForeground(Color.BLACK);
        lblRes.setForeground(Color.BLACK);
    if(p<10.5){
        txtPromFinal.setForeground(Color.RED);
        m="You Lose!";
        lblMensaje.setText("");
    }
    else{
        txtPromFinal.setForeground(Color.BLACK);
        m="You Win!";
        lblMensaje.setText("");
    }
}
else if(opc=='n'){
    txtExF.setForeground(Color.BLUE);
    txtPromFinal.setForeground(Color.GREEN);
    if(p>=10.5){
        m="You Win!!";
        lblMensaje.setText("");
        lblMensaje.setForeground(Color.yellow);
        lblRes.setForeground(Color.YELLOW);
    }
    else{
        m="You Lose!!";
        lblMensaje.setText("");
        lblMensaje.setForeground(Color.RED);
        lblRes.setForeground(Color.RED);
    }
}
    lblRes.setText(m);
    txtPromFinal.setText(p+"");
}
