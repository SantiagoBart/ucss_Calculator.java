    private void validar(java.awt.event.KeyEvent evt,String t){
        char validar=evt.getKeyChar();
        if(Character.isLetter(validar)){
            evt.consume();
            JOptionPane.showMessageDialog(PanelContinuas,evt.getKeyChar()+" \nIngrese solo números");            
        }
        if(!Character.isDigit(evt.getKeyChar())&&evt.getKeyChar()!='.'){
            evt.consume();
        }
        if(evt.getKeyChar()=='.'&&t.contains(".")){
            evt.consume();
        }
    }
