    private void continuas(int x){
    int y=0;
    while(y<8){
        if(y<x){
            n.get(y).setEnabled(true);
        }
        else{
            n.get(y).setEnabled(false);
        }
    y++;
    }
}
    private void promC(){
    int c=0;
    double p=0;
    for(int i=0;i<n.size();i++){
        if(n.get(i).isEnabled()){
            c=c+1;
            p=p+Double.parseDouble(n.get(i).getText());
        }
    }
    txtContinua.setText(""+p/c);
    }
